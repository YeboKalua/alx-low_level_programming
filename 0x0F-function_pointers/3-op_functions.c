#include "3-calc.h"
/**
 * op_add - adds numbers
 * @a: integer a
 * @b: integer b
 * Return: result int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts numbers
 * @a: integer a
 * @b: integer b
 * Return: result int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - subtracts numbers
 * @a: integer a
 * @b: integer b
 * Return: result int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - subtracts numbers
 * @a: integer a
 * @b: integer b
 * Return: result int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - subtracts numbers
 * @a: integer a
 * @b: integer b
 * Return: result int
 */
int op_mod(int a, int b)
{
	return (a % b);
}

